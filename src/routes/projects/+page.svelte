<section>
	<hgroup>## Eurydice discord bot for managing TTRPG groups</hgroup>

	<a href="https://github.com/drowrin/eurydice">https://github.com/drowrin/eurydice</a> This isn't a
	dice roller or a any form of rules automation. There are endless solutions out there for that. This
	is an organization tool. It's designed to help automate some common tasks and provide as much convenience
	as possible. It has features for automatically including character names in discord nicknames when
	a session is live, managing session-zero type notes like safety tools, scheduling sessions and performing
	"ready checks", and so on.
</section>
